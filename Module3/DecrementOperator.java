package Module3;

public class DecrementOperator {
    
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Decrement Operator!"); 
        System.out.println("Initial Value "+a);
        a--; //post Decrement
        System.out.println("Post Decrement Value "+a);
        --a; //pre Decrement
        System.out.println("Pre Decrement Values "+a);
    }
}