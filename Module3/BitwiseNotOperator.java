package Module3;
/*
 * The bitwise complement operator (~) in Java is a unary operator that inverts all the bits of its operand. It flips every 0 to 1 and every 1 to 0. 
 * 
 */
public class BitwiseNotOperator {
    public static void main(String[] args) {
        int a = 5; //8421. 0101
                   //      1010  
        System.out.println("Bitwise Not Operator "+ ~a);
    }
}
